import { bigNumberString } from "./bigNumberString"

describe('bigNumberString', ()=>{
    it('should return the right texts', ()=>{
        expect(bigNumberString(0)).toBe('0')
        expect(bigNumberString(1)).toBe('1')
        expect(bigNumberString(10)).toBe('10')
        expect(bigNumberString(100)).toBe('100')
        expect(bigNumberString(1000)).toBe('1K')
        expect(bigNumberString(1002)).toBe('1K')
        expect(bigNumberString(1500)).toBe('1.5K')
        expect(bigNumberString(1510)).toBe('1.5K')
        expect(bigNumberString(10000)).toBe('10K')
        expect(bigNumberString(10200)).toBe('10K')
        expect(bigNumberString(10500)).toBe('10.5K')
        expect(bigNumberString(10510)).toBe('10.5K')
        expect(bigNumberString(100000)).toBe('100K')
        expect(bigNumberString(100200)).toBe('100K')
        expect(bigNumberString(100500)).toBe('100.5K')
        expect(bigNumberString(100510)).toBe('100.5K')
        expect(bigNumberString(1000000)).toBe('1M')
        expect(bigNumberString(1000002)).toBe('1M')
        expect(bigNumberString(1500000)).toBe('1.5M')
        expect(bigNumberString(1510000)).toBe('1.5M')
        expect(bigNumberString(10000000)).toBe('10M')
        expect(bigNumberString(10200000)).toBe('10M')
        expect(bigNumberString(10500000)).toBe('10.5M')
        expect(bigNumberString(10510000)).toBe('10.5M')
        expect(bigNumberString(100000000)).toBe('100M')
        expect(bigNumberString(100200000)).toBe('100M')
        expect(bigNumberString(100500000)).toBe('100.5M')
        expect(bigNumberString(100510000)).toBe('100.5M')
        expect(bigNumberString(1000000000)).toBe('1B')
        expect(bigNumberString(1000000002)).toBe('1B')
        expect(bigNumberString(1500000000)).toBe('1.5B')
        expect(bigNumberString(1510000000)).toBe('1.5B')
        expect(bigNumberString(10000000000)).toBe('10B')
        expect(bigNumberString(10200000000)).toBe('10B')
        expect(bigNumberString(10500000000)).toBe('10.5B')
        expect(bigNumberString(10510000000)).toBe('10.5B')
        expect(bigNumberString(100000000000)).toBe('100B')
        expect(bigNumberString(100200000000)).toBe('100B')
        expect(bigNumberString(100500000000)).toBe('100.5B')
        expect(bigNumberString(100510000000)).toBe('100.5B')
    })
})